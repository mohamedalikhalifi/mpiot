echo \
"##############################################################
# UPDATE SYSTEM
##############################################################"
apt-get update 
apt-get upgrade

echo \
"##############################################################
# ENABLE SSH
##############################################################"
systemctl enable ssh
systemctl start ssh

echo \
"##############################################################
# ENABLE CAMERA MODULE
##############################################################"
sed -i "s/start_x=0/start_x=1/g" /boot/config.txt

echo \
"##############################################################
# GET DOCKER
##############################################################"
curl -fsSL https://get.docker.com -o get-docker.sh
sh get-docker.sh

echo \
"##############################################################
# GET GIT
##############################################################"
apt-get install git

echo \
"##############################################################
# DOWNLOAD WORKDIR
##############################################################"
mkdir ~\workspace
cd ~\workspace
wget -c https://gitlab.com/khalifima/mpiot/-/archive/main/mpiot-main.tar.gz -O - | tar -xz

echo \
"##############################################################
# REBOOTING Device
##############################################################"
reboot